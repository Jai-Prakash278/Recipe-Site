function showSidebar(){
    const sidebar = document.querySelector('#menulist,#apear')
    menulist.style.display = 'flex'
    apear.style.display = 'block'
}
function hideSidebar(){
    const sidebar = document.querySelector('#menulist, #apear')
    menulist.style.display = 'none'
    apear.style.display = 'none'
}